package de.hwrberlin.bidhub.util;

/**
 * Enum, das die verschiedenen FXML-Dateinamen repräsentiert, die in der Anwendung verwendet werden.
 * Diese Enumerationswerte werden verwendet, um die Navigation innerhalb der Anwendung zu verwalten und
 * die entsprechenden FXML-Dateien für die Benutzeroberfläche zu laden.
 */
public enum FxmlFile {
    Login,
    Register,
    Dashboard,
    AuctionRoom,
    ChatMessage,
    Overview,
    JoinAuction,
    AuctionRoomPreview,
    CreateAuction,
    StartAuctionPopup,
    InfoPopup,
    UserInformation,
    RoomInformation
}
